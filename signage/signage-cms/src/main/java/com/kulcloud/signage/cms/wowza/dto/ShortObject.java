package com.kulcloud.signage.cms.wowza.dto;

public class ShortObject {

	private String id = null;
	private String href = null;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

}
